# Script para imprimir los nombres y apellidos de los integrantes del grupo

def imprimir_integrantes():
    integrantes = [
        "Diego Vera",
        "Jorge Quidel",
        "Leonardo Muñoz"
    ]

    print("Nombres y apellidos de los integrantes del grupo:")
    for integrante in integrantes:
        print("- " + integrante)

# Llamada a la función para imprimir los integrantes
imprimir_integrantes()

